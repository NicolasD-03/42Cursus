#include <stdio.h>
#include <ctype.h>

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}

int	main(void)
{
	int	c;

	c = 70;
	printf("%d", ft_isalpha(c));
	return (0);
}